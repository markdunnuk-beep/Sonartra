export function generateAllProfiles(): readonly SimulatedProfile[] {
  const [operatingStyle, coreDrivers, leadershipApproach, tensionResponse, environmentFit, pressureResponse] =
    DOMAIN_PAIR_SETS;

  const profiles: SimulatedProfile[] = [];
  let profileNumber = 1;

  for (const operatingStylePair of operatingStyle.pairKeys) {
    for (const coreDriversPair of coreDrivers.pairKeys) {
      for (const leadershipApproachPair of leadershipApproach.pairKeys) {
        for (const tensionResponsePair of tensionResponse.pairKeys) {
          for (const environmentFitPair of environmentFit.pairKeys) {
            for (const pressureResponsePair of pressureResponse.pairKeys) {
              profiles.push({
                profileKey: `generated_${String(profileNumber).padStart(5, '0')}`,
                domainPairs: {
                  operatingStyle: operatingStylePair,
                  coreDrivers: coreDriversPair,
                  leadershipApproach: leadershipApproachPair,
                  tensionResponse: tensionResponsePair,
                  environmentFit: environmentFitPair,
                  pressureResponse: pressureResponsePair,
                },
              });
              profileNumber += 1;
            }
          }
        }
      }
    }
  }

  return profiles;
}
